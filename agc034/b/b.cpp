int main(int argc, char** argv) {
  string s;
  cin >> s;

  ll cnt = 0;
  ll ans = 0;
  for (int i = 0; i < s.size(); ++i) {
    if (s[i] == 'B' && s[i+1] == 'C') {  // "BC" pair.
      ans += cnt;
      ++i; // We consider this as pair, so skip C.
    } else if (s[i] == 'A') {
      ++cnt;
    } else {  // "B" or "C" alone.
      cnt = 0;  // reset count.
    }
  }

  cout << ans << endl;
}
