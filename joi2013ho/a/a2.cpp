int main(int argc, char** argv) {
  cin.tie(NULL);
  cout.tie(NULL);
  ios_base::sync_with_stdio(false);
  //cout << setprecision(10) << fixed;

  ll n;
  cin >> n;
  vector<ll> a(n);
  rep(i,n) {
    cin >> a[i];
  }

  vector<ll> b; // length of 1-0-1-...
  {
    ll i = 0;
    while (i<n) {
      ll j = i+1;
      while (j<n && a[j] != a[j-1]) {
        ++j;
      }
      // Here, a[j]==a[j-1]. length is j-i.
      b.emplace_back(j-i);
      i=j;
    }
  }

  // printvec(a);
  // printvec(b);

  ll ans = 0;
  ll m = b.size();
  rep(i,m) {
    ll now = b[i];
    if (i>0) {
      now += b[i-1];
    }
    if (i<m-1) {
      now += b[i+1];
    }
    chmax(ans,now);
  }
  cout << ans << endl;
}
